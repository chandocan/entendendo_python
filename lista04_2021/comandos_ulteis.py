# criar uma lista de uma string com split
n = 'jose Alfredo dos santos filho'.split()
print(n)
# troca um elemento da lista usando o indice
n[4] = 'neto'
print(n)
# adiciona elemento  no final da lista
n.append('leão')
print(n)
# iseri um elemento na posição zero da lista
n.insert(0,'zeus')
print(n)
